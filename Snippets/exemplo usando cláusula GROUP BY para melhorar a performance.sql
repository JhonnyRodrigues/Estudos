H.ID_HISTORICO
FROM
	HELP_HISTORICOS H
JOIN (
		SELECT
			H2.FK_SOLICITACAO,
			MAX(H2.ID_HISTORICO) AS ABACATE
		FROM
			HELP_HISTORICOS H2
		GROUP BY
			H2.FK_SOLICITACAO
		ORDER BY
			H2.FK_SOLICITACAO ASC
	) S ON
	H.ID_HISTORICO = S.ABACATE
